class Node :
    def __init__(self) :
        self.inputs = []
        self.weights = []
        self.bias = 0
        self.output = 0
        
    def forward_pass(self,inputs) :
        for i in range(len(self.inputs)) :
            self.output += self.inputs[i] * self.weights[i] 
        self.output += self.bias
    
    def activate(self):
        if self.output < 0:
            self.output = 0

